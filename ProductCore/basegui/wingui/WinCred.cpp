int rounddiv(double a, double b)
{
    return int(a / b);
}
int rounddiv(int a, int b)
{
    int r = a % b;
    if (r > b/2)
        return a/b + 1;
    else
        return a/b;
}

void CWinCreditsCtrl::Scroll()
{
    if (m_speedy > SCROLL_SPEED) 
    {
        m_speedy -= std::max<speed_t>(SCROLL_ACC, m_speedy/(m_accmode?SPEED_DECAY1:SPEED_DECAY2));
        if (m_speedy < SCROLL_SPEED) 
            m_speedy = SCROLL_SPEED;
    }
    else if (m_speedy < SCROLL_SPEED)
    {
        m_speedy += std::max<speed_t>(SCROLL_ACC, -m_speedy/(m_accmode?SPEED_DECAY1:SPEED_DECAY2));
        if (m_speedy > SCROLL_SPEED) 
            m_speedy = SCROLL_SPEED;
    }

    m_scroll += m_speedy * SLEEP_DELAY;
	m_nScrollY = (int)rounddiv(m_scroll, SPEED_QUANTIFIER);
	//m_nBlendCaption = (int)(m_scroll/ SPEED_QUANTIFIER);

    NormalizeScroll();
}

void CWinCreditsCtrl::NormalizeScroll()
{
    if      ( m_scroll < 0               ) m_scroll += m_szWorkBmp.cy * SPEED_QUANTIFIER;
	else if ( m_scroll >= m_szWorkBmp.cy ) m_scroll -= m_szWorkBmp.cy * SPEED_QUANTIFIER;	

    if      ( m_nScrollY < 0               ) m_nScrollY += m_szWorkBmp.cy;
	else if ( m_nScrollY >= m_szWorkBmp.cy ) m_nScrollY -= m_szWorkBmp.cy;	
}

void CWinCreditsCtrl::SetScroll(int y)
{
    m_scroll += y*SPEED_QUANTIFIER;
	m_nScrollY += y;
	//m_nBlendCaption += y;
    NormalizeScroll();
}

