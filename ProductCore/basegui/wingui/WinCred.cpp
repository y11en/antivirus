int CWinCreditsCtrl::CreditSection2WorkDC(const CreditSection& cs, int top)
{
	int y = top;

	for(int i = 0; i < (int)cs.m_vCredits.size(); i++){
		const CSItem& ci = cs.m_vCredits[i];
		
		if( !ci.m_sname.empty() ){
			RECT rcText = { 0, y + ci.m_margin.cy, m_szWorkBmp.cx, y + ci.m_height + ci.m_margin.cy};
			ci.m_pFont->Draw(m_dc[dc_work], ci.m_sname.c_str(), rcText, DT_VCENTER|DT_LEFT|DT_SINGLELINE); // DT_CENTER
		}

		y += (ci.m_height + 2*ci.m_margin.cy);
	}

	return y;
}

