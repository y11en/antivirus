int CWinCreditsCtrl::GetSectionByScrollY(int ScrollY)
{
	for(size_t i = 0; i < m_vStartSect.size(); i++)
		if( m_vStartSect[i] == ScrollY ) return (int)i;
	return -1;
}

bool CWinCreditsCtrl::GetItemByScrollY(int ScrollY, int& sect, int& item)
{
    int i;
	for(i = 0; i < (int)m_vStartSect.size(); i++)
    {
		if( m_vStartSect[i] <= ScrollY)
        {
            if (i < (int)m_vStartSect.size() - 1 && m_vStartSect[i+1] > ScrollY)
            {
                break;
            }
        }
    }
    if (i < (int)m_vStartSect.size())
    {
        int y = ScrollY - m_vStartSect[i];

        CreditSection &Section = m_vSections[i];
        for (int j = 0; j < (int)Section.m_vCredits.size(); ++j)
        {
            CSItem &Item = Section.m_vCredits[j];
            if (y < Item.m_margin.cy)
            {
                return false;
            }
            y -= Item.m_margin.cy;
            if (y <= Item.m_height)
            {   
                sect = i;
                item = j;
                return true;
            }
            y -= Item.m_height;
            if (y < Item.m_margin.cy)
            {
                return false;
            }
            y -= Item.m_margin.cy;
        }
    }
    return false;
}

