void CWinCreditsCtrl::DrawOneStep(HDC dc, RECT rcFrame)
{
	SIZE sz = { rcFrame.right - rcFrame.left, rcFrame.bottom - rcFrame.top };
	//    
	BitBlt(m_dc[dc_scr], 0, 0, sz.cx, sz.cy, dc, rcFrame.left, rcFrame.top, SRCCOPY);

	//  
	int isection = GetSectionByScrollY(m_nScrollY);
	if( isection != -1 && isection != m_cur_section )
	{
		m_cur_section = isection;
		m_nBlendCaption = 0;
	}
	//      
	if( m_pfnAlphaBlend )
	{
		BLENDFUNCTION bf = { AC_SRC_OVER, 0, 0, 0 };
		if( m_nBlendCaption < 85 ){
			if( m_cur_section > 0 ){
				DrawCaption(dc, rcFrame, m_cur_section - 1);
				bf.SourceConstantAlpha = 255 - 3*m_nBlendCaption;
				m_pfnAlphaBlend(dc, rcFrame.left, rcFrame.top, sz.cx, m_TitleHi, m_dc[dc_capt], 0, 0, sz.cx, m_TitleHi, bf); 
			}
		}
	
		DrawCaption(dc, rcFrame, m_cur_section);
		bf.SourceConstantAlpha = m_nBlendCaption < 122 ? 2*m_nBlendCaption : 255;
		m_pfnAlphaBlend(dc, rcFrame.left, rcFrame.top, sz.cx, m_TitleHi, m_dc[dc_capt], 0, 0, sz.cx, m_TitleHi, bf); 
	}
	else //   
	{
		DrawCaption(dc, rcFrame, m_cur_section);
		BitBlt(dc, rcFrame.left, rcFrame.top, sz.cx, m_TitleHi, m_dc[dc_capt], 0, 0, SRCCOPY);
	}
	
	//   
	BitBlt(m_dc[dc_scr], 0, m_TitleHi, m_szWorkBmp.cx, m_szWorkBmp.cy, m_dc[dc_work], 0, m_nScrollY, SRCINVERT);
	BitBlt(m_dc[dc_scr], 0, m_TitleHi, m_szWorkBmp.cx, m_szWorkBmp.cy, m_dc[dc_mask], 0, m_nScrollY, SRCAND);
	BitBlt(m_dc[dc_scr], 0, m_TitleHi, m_szWorkBmp.cx, m_szWorkBmp.cy, m_dc[dc_work], 0, m_nScrollY, SRCINVERT);
	
	BitBlt(dc, rcFrame.left, rcFrame.top + m_TitleHi, sz.cx, sz.cy - m_TitleHi, m_dc[dc_scr], 0, m_TitleHi, SRCCOPY);

	GdiFlush();
}

void CWinCreditsCtrl::UpdateCreditsDC(HDC dc, RECT rcFrame)
{
	DeleteCompDC(dc_work);
	DeleteCompDC(dc_mask);
	m_vStartSect.clear();

	//   DC
	m_szWorkBmp.cx = rcFrame.right - rcFrame.left;
	m_szWorkBmp.cy = rcFrame.bottom - rcFrame.top;
	size_t i;
	for(i = 0; i < m_vSections.size(); i++)
		m_szWorkBmp.cy += m_vSections[i].CalcTotalHeight();
	
	//   DC
	CreateCompDC(dc, dc_work, m_szWorkBmp.cx, m_szWorkBmp.cy);
	
	RECT rc = { 0, 0, m_szWorkBmp.cx, m_szWorkBmp.cy };
	COLORREF clrOld = ::SetBkColor(m_dc[dc_work], MASK_RGB);
	::ExtTextOut(m_dc[dc_work], 0, 0, ETO_OPAQUE, &rc, NULL, 0, NULL);

	int y = rcFrame.bottom - rcFrame.top;
	for(i = 0; i < m_vSections.size(); i++)
	{
		m_vStartSect.push_back(y - rcFrame.bottom + rcFrame.top);
		y = CreditSection2WorkDC(m_vSections[i], y);
	}

	//  DC 
	CreateCompDC(m_dc[dc_scr], dc_mask, m_szWorkBmp.cx, m_szWorkBmp.cy);
	BitBlt(m_dc[dc_mask], 0, 0, m_szWorkBmp.cx, m_szWorkBmp.cy, m_dc[dc_work], 0, 0, SRCCOPY);
}

void CWinCreditsCtrl::DrawCaption(HDC ParentDc, RECT rcFrame, int isection)
{
	if( isection < 0 || (size_t)isection >= m_vSections.size() )
		return;
	//    
	RECT rc =	{ 0, 0, rcFrame.right - rcFrame.left, m_TitleHi };
	
	BitBlt(m_dc[dc_capt], rc.left, rc.top, rc.right - rc.left, rc.bottom - rc.top, ParentDc, rcFrame.left, rcFrame.top, SRCCOPY);
	
	const CSItem& sci = m_vSections[isection].m_csTitle;
	if( sci.m_pFont )
		sci.m_pFont->Draw(m_dc[dc_capt], sci.m_sname.c_str(), rc, DT_VCENTER|DT_LEFT|DT_SINGLELINE);
}
