int	GetFontHeight(HDC hDc, HFONT hFont)
{
	if( NULL == hDc )
		return 0;

	HFONT oldFont = (HFONT)::SelectObject(hDc, hFont);
	SIZE sz = {0, 0};	
	GetTextExtentPoint32(hDc, "W", 1, &sz);
	if( (sz.cy%2) != 0  ) sz.cy += 1;
	::SelectObject(hDc, oldFont);
	return sz.cy;
}

